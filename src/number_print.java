import java.util.Scanner;
public class number_print	
{
	int k=1;

	public static void main(String args[])
	{
		
		int k=1;
		for (int i=1;i<=5;i++)//column
		{
			for (int j=1;j<=i;j++)//row
			{		
				System.out.print(""+i);
		}
			System.out.print("\n");
			k++;
		}
		System.out.println("");
		
		}
	}


